package Java_basics.Data_types_and_variables.Types_and_variables;

public class Types_and_variables {
    public static void main(String[] args) {
        /**
         * Given two variables a and b with invalid declarations. Add a type to them.
         */

        int a = 512_343;
        int b = 3_431_231;
        System.out.println(a + b);
        /*
        Encoding can be more formal if you use good compilation methods (e.g. _ between numbers to separate them, Spaces before assignment).
        如果使用良好的编译方法可以使编码更加规范（例如数字之间加上_用以分隔，赋值号前使用空格）
         */

        /**
         * Given four variables. Assign values to these variables.
         * The variable one should be 1, two should be 2, three should be 3 and four should be
         */

        int one = 1;
        int two = 2;
        int three = 3;
        int four = 4;

        /*
        切记不要因为一己私利而不遵守编程规则，这样不仅会耽误自己的开发效率，而且会给后来者阅读带来极大的困扰，好的程序是给人读懂的，而不仅仅是计算机
        Remember not to follow the rules of programming because of selfish interests, which will not only delay their own development efficiency, but also will bring great trouble to later readers. Good programs are read by people, not just computers
         */

        /**
         * Given a variable, add the int type and assign the value 123456 to this variable. Do not forget ; at the end of the statement.
         */

        int variable = 123_456; // Change this line
        System.out.println(variable);

        /*
        同上，合理使用空格分隔赋值符，使用_分隔数字使他人阅读方便是极为重要的
        As above, it is extremely important to use Spaces to separate assignments and _ to separate numbers so that others can read them easily

        在IDEA中可以使用Ctrl+Alt+L来快速完成格式规范
         */
    }
}
